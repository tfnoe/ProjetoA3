    public void receberDados(int id) {
        // Preenche os campos de texto com os dados do projeto
        String sql = "SELECT * FROM tbl_projeto WHERE id_projeto = ?";

        try (Connection conexao = new ConexaoDAO().conectaBD(); PreparedStatement stmt = conexao.prepareStatement(sql)) {

            stmt.setInt(1, id);

            try (ResultSet rs = stmt.executeQuery()) {

                if (rs.next()) {

                    this.txt_idprojeto.setText(String.valueOf(id));
                    this.txt_nmprojeto.setText(rs.getString("nm_projeto"));
                    this.txt_descricaoprojeto.setText(rs.getString("desc_projeto"));

                    java.sql.Date dtInicio = rs.getDate("dt_inicioprojeto");
                    java.sql.Date dtFim = rs.getDate("dt_fimprojeto");

                    java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
                    if (dtInicio != null) {
                        this.txt_dtinicio.setText(sdf.format(dtInicio));
                    }
                    if (dtFim != null) {
                        this.txt_dtfim.setText(sdf.format(dtFim));
                    }

                } else {

                    JOptionPane.showMessageDialog(this, "Projeto com ID " + id + " não encontrado.", "Aviso", JOptionPane.WARNING_MESSAGE);
                }
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar dados do projeto:\n" + e.getMessage(), "Erro de Banco de Dados", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
