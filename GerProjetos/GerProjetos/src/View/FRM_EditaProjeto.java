    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
        // 1. OBTER DADOS DA TELA
        String idStr = txt_idprojeto.getText();
        String nome = txt_nmprojeto.getText();
        String descricao = txt_descricaoprojeto.getText();
        String dataInicioStr = txt_dtinicio.getText();
        String dataFimStr = txt_dtfim.getText();

        // 2. VALIDAÇÃO BÁSICA
        if (nome.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "O campo 'Nome Projeto' é obrigatório.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            txt_nmprojeto.requestFocus(); // Foca no campo para o usuário corrigir
            return; // Para a execução do método aqui
        }

        // 3. PARSE DE DATAS (Converter Texto para Objeto Date)
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
        java.util.Date dataInicioUtil = null;
        java.util.Date dataFimUtil = null;

        try {
            // Apenas tenta converter se o campo não estiver vazio ou com a máscara
            if (!dataInicioStr.trim().replace("/", "").replace("_", "").isEmpty()) {
                dataInicioUtil = sdf.parse(dataInicioStr);
            }
            if (!dataFimStr.trim().replace("/", "").replace("_", "").isEmpty()) {
                dataFimUtil = sdf.parse(dataFimStr);
            }
        } catch (java.text.ParseException e) {
            JOptionPane.showMessageDialog(this, "Formato de data inválido. Use o formato DD/MM/AAAA.", "Erro de Formato", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 4. LÓGICA DE INSERT VS UPDATE
        String sql;
        boolean isInsert = idStr.trim().isEmpty();

        if (isInsert) {
            // Novo projeto (INSERT)
            sql = "INSERT INTO tbl_projeto (nm_projeto, desc_projeto, dt_inicioprojeto, dt_fimprojeto) VALUES (?, ?, ?, ?)";
        } else {
            // Projeto existente (UPDATE)
            sql = "UPDATE tbl_projeto SET nm_projeto = ?, desc_projeto = ?, dt_inicioprojeto = ?, dt_fimprojeto = ? WHERE id_projeto = ?";
        }

        // 5. EXECUÇÃO NO BANCO DE DADOS
        try (java.sql.Connection conexao = new ConexaoDAO().conectaBD(); java.sql.PreparedStatement stmt = conexao.prepareStatement(sql)) {

            // Definir os parâmetros do PreparedStatement
            stmt.setString(1, nome);
            stmt.setString(2, descricao);

            // Parâmetro da Data de Início
            if (dataInicioUtil != null) {
                stmt.setDate(3, new java.sql.Date(dataInicioUtil.getTime()));
            } else {
                stmt.setNull(3, java.sql.Types.DATE);
            }

            // Parâmetro da Data de Fim
            if (dataFimUtil != null) {
                stmt.setDate(4, new java.sql.Date(dataFimUtil.getTime()));
            } else {
                stmt.setNull(4, java.sql.Types.DATE);
            }

            // Se for UPDATE, definir o ID no final da query
            if (!isInsert) {
                int id = Integer.parseInt(idStr);
                stmt.setInt(5, id);
            }

            // Executar a query
            int resultado = stmt.executeUpdate();

            if (resultado > 0) {
                JOptionPane.showMessageDialog(this, "Projeto salvo com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

                this.dispose(); 
            }

        } catch (java.sql.SQLException | NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar projeto no banco de dados:\n" + e.getMessage(), "Erro de Banco de Dados", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_salvarActionPerformed

